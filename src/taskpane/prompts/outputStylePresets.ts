export function isOutputStyleActive(
  styleId?: string
): styleId is Exclude<OutputStyleId, "default"> {
  return !!styleId && styleId !== "default" && OUTPUT_STYLE_PRESETS.some((item) => item.id === styleId);
}

export function resolveOutputStyleId(value: unknown): OutputStyleId {
  if (value === undefined || value === null || value === "") {
    return DEFAULT_OUTPUT_STYLE_ID;
  }
  if (value === "default") {
    return DEFAULT_OUTPUT_STYLE_ID;
  }
  if (typeof value === "string" && OUTPUT_STYLE_PRESETS.some((item) => item.id === value)) {
    return value as OutputStyleId;
  }
  return DEFAULT_OUTPUT_STYLE_ID;
}

export function getOutputStylePreset(id: Exclude<OutputStyleId, "default">): OutputStylePreset {
  return OUTPUT_STYLE_PRESETS.find((item) => item.id === id) ?? OUTPUT_STYLE_PRESETS[0];
}

export function getOutputStyleDisplay(id?: string): { label: string; description: string } {
  if (!isOutputStyleActive(id)) {
    return DEFAULT_OUTPUT_STYLE_OPTION;
  }
  const preset = getOutputStylePreset(id);
  return { label: preset.label, description: preset.description };
}

export function appendOutputStylePrompt(
  basePrompt: string,
  styleId: OutputStyleId | undefined,
  hasSelection: boolean
): string {
  if (!isOutputStyleActive(styleId)) {
    return basePrompt.trim();
  }

  const preset = getOutputStylePreset(styleId);
  const addon = hasSelection ? preset.withSelection : preset.withoutSelection;

  const styleBlock = [
    "═══════════════════════════════════════",
    `【最高优先级·输出风格：${preset.label}】`,
    `风格指纹：${preset.fingerprint}`,
    addon,
    `【风格自检】`,
    `1. 若把「${preset.label}」换成其他任一风格，读起来是否应完全不同？若否，重写。`,
    `2. 是否包含本风格的必用特征？是否触犯硬性禁止项？`,
    `3. 是否仍带「专业 Word 助手」默认腔？若是，重写。`,
    "═══════════════════════════════════════",
    `【输出前最后一句话】${preset.finalReminder}`,
  ].join("\n");

  return `${basePrompt.trim()}\n\n${styleBlock}`.trim();
}
